#!/usr/bin/env zsh

# Run docker compose with whichever CLI flavour is available.
_DockerCompose() {
  if docker compose version >/dev/null 2>&1; then
    docker compose "$@"
  elif command -v docker-compose >/dev/null 2>&1; then
    docker-compose "$@"
  else
    EchoFatal "docker compose not found; please install Docker Desktop or docker-compose"
    return 1
  fi
}

_CheckDockerCompose() {
  if docker compose version >/dev/null 2>&1; then
    return 0
  elif command -v docker-compose >/dev/null 2>&1; then
    return 0
  fi
  return 1
}

# Write a docker-compose file tailored for a single plotting run.
# $1 = plots submodule dir (absolute)
# $2 = results dir          (absolute, mounted read-only as /data)
# $3 = experiment dir       (absolute, mounted read-write as /output)
# $4 = destination file
_WritePlotsComposeFile() {
  local plots_dir="$1"
  local results_dir="$2"
  local experiment_dir="$3"
  local dest="$4"

  # Use printf so we control every byte; no heredoc quoting surprises.
  printf '%s\n' \
    '# Generated by mkexp2 plot -- do not edit manually' \
    'services:' \
    '  plot:' \
    '    build:' \
    "      context: ${plots_dir}" \
    '      dockerfile: Dockerfile' \
    '    working_dir: /work' \
    '    volumes:' \
    "      - ${plots_dir}:/work:ro" \
    "      - ${results_dir}:/data:ro" \
    "      - ${experiment_dir}:/output" \
    "      - ${plots_dir}/.r-libs:/usr/local/lib/R/site-library" \
    "      - ${plots_dir}/.cache:/cache" \
    '    environment:' \
    '      - TZ=Europe/Berlin' \
    '    init: true' \
    > "$dest"
}

GeneratePlots() {
  local plots_dir="$MKEXP2_HOME/plots"
  local results_dir="$PWD/results"
  local experiment_dir="$PWD"

  # ── Validate prerequisites ──────────────────────────────────────────────────

  if [[ ! -f "$plots_dir/common.R" ]]; then
    EchoFatal "plots submodule not initialized at $plots_dir"
    EchoFatal "Run: git submodule update --init plots"
    return 1
  fi

  if ! _CheckDockerCompose; then
    EchoFatal "docker compose not found; please install Docker Desktop or docker-compose"
    return 1
  fi

  # ── Determine active algorithms ─────────────────────────────────────────────

  local -a active_algos=()

  if (( ${#MKEXP2_PLOT_ALGORITHMS[@]} > 0 )); then
    active_algos=("${MKEXP2_PLOT_ALGORITHMS[@]}")
  else
    # Collect algorithms from every Experiment* function, deduplicated.
    local -a exp_fns=()
    exp_fns=($(DiscoverExperimentFunctions "$PWD/Experiment"))

    if (( ${#exp_fns[@]} == 0 )); then
      EchoFatal "no Experiment*() functions found in Experiment"
      return 1
    fi

    local fn="" algo=""
    for fn in "${exp_fns[@]}"; do
      LoadExperimentFunctionState "$PWD/Experiment" "$fn"
      for algo in "${_algorithms[@]}"; do
        if (( ${active_algos[(Ie)$algo]} == 0 )); then
          active_algos+=("$algo")
        fi
      done
    done
  fi

  if (( ${#active_algos[@]} == 0 )); then
    EchoWarn "no algorithms found; nothing to plot"
    return 0
  fi

  EchoStep "Active algorithms: ${(j:, :)active_algos}"

  # ── Warn about missing CSV files ────────────────────────────────────────────

  local algo=""
  for algo in "${active_algos[@]}"; do
    if [[ ! -f "$results_dir/${algo}.csv" ]]; then
      EchoWarn "CSV not found: $results_dir/${algo}.csv (run mkexp2 parse first)"
    fi
  done

  # ── Determine which plots to generate ──────────────────────────────────────

  local do_pp=$MKEXP2_PLOT_PERFORMANCE_PROFILE
  local do_speedup=$MKEXP2_PLOT_SPEEDUP
  local do_rt=$MKEXP2_PLOT_RUNNING_TIME

  if (( !MKEXP2_PLOT_EXPLICIT_SELECTION )); then
    do_pp=1
    do_speedup=1
    do_rt=1
  fi

  # ── Generate docker-compose file ────────────────────────────────────────────

  mkdir -p "$PWD/.mkexp2"
  local compose_file="$PWD/.mkexp2/plots-compose.yml"
  _WritePlotsComposeFile "$plots_dir" "$results_dir" "$experiment_dir" "$compose_file"

  # ── Build Docker image ──────────────────────────────────────────────────────

  EchoStep "Building plots Docker image (this is slow the first time)"
  if ! _DockerCompose -f "$compose_file" build plot; then
    EchoFatal "failed to build plots Docker image"
    return 1
  fi

  # ── Install R packages (cached in plots/.r-libs) ───────────────────────────

  mkdir -p "$plots_dir/.r-libs"
  if [[ -z "$(ls -A "$plots_dir/.r-libs" 2>/dev/null)" ]]; then
    EchoStep "Installing R packages into $plots_dir/.r-libs (first run, this takes a while)"
    if ! _DockerCompose -f "$compose_file" run --rm plot \
        Rscript /work/install.R; then
      EchoFatal "R package installation failed"
      return 1
    fi
  fi

  # ── Build Rscript argument list ─────────────────────────────────────────────

  local -a r_args=()
  (( do_pp ))      && r_args+=("--performance-profile")
  (( do_speedup )) && r_args+=("--speedup")
  (( do_rt ))      && r_args+=("--running-time")
  r_args+=("--output" "/output/plots.pdf")
  r_args+=("${active_algos[@]}")

  # ── Run mkplots.R inside the container ─────────────────────────────────────

  EchoStep "Generating plots"
  if ! _DockerCompose -f "$compose_file" run --rm plot \
      Rscript /work/mkplots.R "${r_args[@]}"; then
    EchoFatal "plot generation failed"
    return 1
  fi

  # ── Ensure plots.pdf is ignored by git ─────────────────────────────────────

  local gitignore_file="$PWD/.gitignore"
  if [[ -f "$gitignore_file" ]] && ! grep -qxF "plots.pdf" "$gitignore_file"; then
    printf '%s\n' "plots.pdf" >> "$gitignore_file"
  fi

  EchoStep "Done: $PWD/plots.pdf"
}
